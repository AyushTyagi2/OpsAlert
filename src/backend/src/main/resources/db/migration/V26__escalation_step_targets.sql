-- ---------------------------------------------------------------------------
-- escalation_step_targets  (who gets notified at this step)
-- ---------------------------------------------------------------------------
CREATE TABLE escalation_step_targets (
    id                  UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    escalation_step_id  UUID NOT NULL REFERENCES escalation_steps(id) ON DELETE CASCADE,
    target_type         escalation_target_type NOT NULL,
    target_id           UUID NOT NULL,               -- user_id / team_id / schedule_id
    notification_channel notification_channel NOT NULL DEFAULT 'EMAIL',
    urgency             VARCHAR(20) NOT NULL DEFAULT 'HIGH'  -- HIGH / LOW
);

