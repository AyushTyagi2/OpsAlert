-- ---------------------------------------------------------------------------
-- incidents
-- ---------------------------------------------------------------------------
CREATE TABLE incidents (
    id                      UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    org_id                  UUID          NOT NULL REFERENCES organizations(id),
    sequence_number         BIGINT        NOT NULL,  -- human-readable INC-00042
    title                   VARCHAR(512)  NOT NULL,
    description             TEXT,
    severity                severity_level NOT NULL DEFAULT 'SEV3',
    status                  incident_status NOT NULL DEFAULT 'TRIGGERED',
    escalation_policy_id    UUID          REFERENCES escalation_policies(id),
    team_id                 UUID          REFERENCES teams(id),
    -- Timing KPIs (denormalised for fast analytics)
    triggered_at            TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    acknowledged_at         TIMESTAMPTZ,
    identified_at           TIMESTAMPTZ,
    resolved_at             TIMESTAMPTZ,
    closed_at               TIMESTAMPTZ,
    -- SLO tracking
    slo_breach              BOOLEAN       NOT NULL DEFAULT FALSE,
    -- Resolution
    resolution_summary      TEXT,
    postmortem_url          TEXT,
    -- Slack/collaboration bridge
    slack_channel_id        VARCHAR(100),
    slack_channel_url       TEXT,
    -- Metadata
    labels                  JSONB         NOT NULL DEFAULT '{}',
    custom_fields           JSONB         NOT NULL DEFAULT '{}',
    created_by_user_id      UUID          REFERENCES users(id),
    created_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    updated_at              TIMESTAMPTZ   NOT NULL DEFAULT NOW(),
    deleted_at              TIMESTAMPTZ
);


