-- ---------------------------------------------------------------------------
-- incident_escalations  (tracks each escalation step firing)
-- ---------------------------------------------------------------------------
CREATE TABLE incident_escalations (
    id                      UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    incident_id             UUID NOT NULL REFERENCES incidents(id) ON DELETE CASCADE,
    escalation_policy_id    UUID NOT NULL REFERENCES escalation_policies(id),
    escalation_step_id      UUID NOT NULL REFERENCES escalation_steps(id),
    step_number             INTEGER NOT NULL,
    triggered_at            TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    acknowledged_at         TIMESTAMPTZ,
    timed_out_at            TIMESTAMPTZ,
    notified_user_ids       UUID[] NOT NULL DEFAULT '{}',
    is_final_step           BOOLEAN NOT NULL DEFAULT FALSE
);



