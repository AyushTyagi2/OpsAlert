-- ---------------------------------------------------------------------------
-- incident_alert_links  (many-to-many: incidents ↔ alerts)
-- ---------------------------------------------------------------------------
CREATE TABLE incident_alert_links (
    incident_id     UUID NOT NULL REFERENCES incidents(id) ON DELETE CASCADE,
    alert_id        UUID NOT NULL,   -- no direct FK to partitioned table
    alert_created_at TIMESTAMPTZ NOT NULL,  -- needed to identify the partition
    linked_at       TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    linked_by_user_id UUID REFERENCES users(id),
    PRIMARY KEY (incident_id, alert_id)
);



